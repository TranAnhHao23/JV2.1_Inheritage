package com.triangle;

public class Shape {
    private double a = 1;
    private double b = 1;

    public Shape() {
    }

    public Shape(double a, double b) {
        this.a = a;
        this.b = b;
    }

    public double getA() {
        return a;
    }

    public void setA(double a) {
        this.a = a;
    }

    public double getB() {
        return b;
    }

    public void setB(double b) {
        this.b = b;
    }
}
